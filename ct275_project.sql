DROP TABLE IF EXISTS CHI_TIET_DON_HANG, DON_HANG, SAN_PHAM, KHUYEN_MAI, LOAI_SAN_PHAM, QUAN_TRI_VIEN, CUA_HANG, KHACH_HANG CASCADE;

CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TABLE KHACH_HANG (
    KH_MA SERIAL PRIMARY KEY,
    KH_TEN VARCHAR(150) NOT NULL,
    KH_EMAIL VARCHAR(150) UNIQUE,
    KH_SDT VARCHAR(20),
    KH_DIACHI TEXT,
    KH_MATKHAU VARCHAR(255),
    KH_NGAYTAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE CUA_HANG (
    CH_MA SERIAL PRIMARY KEY,
    CH_TEN VARCHAR(255) NOT NULL,
    CH_DIACHI TEXT NOT NULL,
    CH_QUANHUYEN VARCHAR(255),
    CH_THANHPHO VARCHAR(255),
    CH_SDT VARCHAR(20),
    MATINH VARCHAR(20),
    MAHUYEN VARCHAR(20),
    MAXA VARCHAR(20),
    SONHA_DUONG VARCHAR(255),
    GIO_MO_CUA VARCHAR(20),
    GIO_DONG_CUA VARCHAR(20),
    CH_TRANGTHAI BOOLEAN DEFAULT false,
    CH_NGAYTAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE LOAI_SAN_PHAM (
    L_MA SERIAL PRIMARY KEY,
    L_TEN VARCHAR(255) NOT NULL,
    L_NGAYTAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE KHUYEN_MAI (
    KM_MA SERIAL PRIMARY KEY,
    KM_TEN VARCHAR(255) NOT NULL,
    KM_MOTA TEXT,
    KM_PHANTRAM INT NOT NULL CHECK (KM_PHANTRAM BETWEEN 1 AND 100),
    KM_NGAYBATDAU DATE NOT NULL,
    KM_NGAYKETTHUC DATE NOT NULL,
    KM_TRANGTHAI BOOLEAN DEFAULT true,
    KM_NGAYTAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE SAN_PHAM (
    SP_MA SERIAL PRIMARY KEY,
    SP_TEN VARCHAR(255) NOT NULL,
    SP_GIA NUMERIC(12,2) NOT NULL,
    SP_MOTA TEXT,
    SP_HINH VARCHAR(255),
    L_MA INT REFERENCES LOAI_SAN_PHAM(L_MA) ON DELETE SET NULL,
    KM_MA INT REFERENCES KHUYEN_MAI(KM_MA) ON DELETE SET NULL,
    SP_NGAYTAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    SP_NGAYCAPNHAT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

